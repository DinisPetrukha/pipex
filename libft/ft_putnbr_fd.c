#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char			c;
	long long int	num;

	num = n;
	if (num == -2147483647)
	{
		write(fd, "-2147483647", 11);
		return ;
	}
	else if (num < 0)
	{
		write(fd, "-", 1);
		num = -num;
	}
	if (num > 9)
		ft_putnbr_fd(num / 10, fd);
	c = num % 10 + '0';
	write(fd, &c, 1);
}
